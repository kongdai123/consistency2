    @torch.no_grad()
    def encode_imgs(self, imgs, permute = False):
        if permute:
            imgs = imgs.permute(0,3,1,2)
        
        # imgs: [B, 3, H, W]
        imgs = 2 * imgs - 1

        posterior = self.vae.encode(imgs).latent_dist
        latents = posterior.sample() * self.vae.config.scaling_factor

        return latents
    
    @torch.no_grad()
    def decode_latents(self, latents, normalize = True, clamp = True, permute = False):
        imgs = self.vae.decode(latents / self.vae.config.scaling_factor).sample
        if normalize:
            imgs = (imgs / 2 + 0.5)
            if clamp:
                imgs = imgs.clamp(0, 1)
        if permute:
            imgs = imgs.permute(0,2,3,1)
        return imgs
    
